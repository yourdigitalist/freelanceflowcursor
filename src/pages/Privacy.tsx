type BlockType = 'p' | 'h1' | 'h2' | 'li';

function renderBlock(type: BlockType, text: string, index: number) {
  if (text === 'Lance') {
    return (
      <h1 key={index} className="text-3xl font-extrabold tracking-tight text-foreground pb-3 text-center">
        {text}
      </h1>
    );
  }

  if (text === 'Privacy Policy') {
    return (
      <h2 key={index} className="text-2xl font-bold text-foreground pb-3 text-center">
        {text}
      </h2>
    );
  }

  if (text.startsWith('Last updated:')) {
    return (
      <p key={index} className="text-sm font-semibold text-muted-foreground pb-10 text-center">
        {text}
      </p>
    );
  }

  if (type === 'h1') {
    return (
      <h3 key={index} className="text-lg font-bold text-foreground pt-10 pb-5">
        {text}
      </h3>
    );
  }

  if (type === 'h2') {
    return (
      <h4 key={index} className="text-base font-semibold text-foreground pt-6 pb-4">
        {text}
      </h4>
    );
  }

  if (type === 'li') {
    return (
      <p key={index} className="ml-5 text-sm leading-7 text-foreground pb-4">
        {'\u2022'} {text}
      </p>
    );
  }

  if (text === 'Jurisdiction-Specific Addenda') {
    return (
      <h3 key={index} className="text-lg font-bold text-foreground pt-10 pb-5 text-center">
        {text}
      </h3>
    );
  }

  return (
    <p key={index} className="text-sm leading-7 text-foreground pb-5">
      {text}
    </p>
  );
}

export default function Privacy() {
  const blocks = PRIVACY_DOC.split('\n')
    .map((line) => {
      const separator = line.indexOf('|');
      if (separator === -1) return null;
      const type = line.slice(0, separator) as BlockType;
      const text = line.slice(separator + 1);
      return { type, text };
    })
    .filter((block): block is { type: BlockType; text: string } => Boolean(block));

  return (
    <div className="min-h-screen bg-background">
      <header className="border-b bg-card/50">
        <div className="container mx-auto px-4 h-14 flex items-center justify-between">
          <AppLogo full height={24} />
          <Link to="/" className="text-sm text-muted-foreground hover:text-foreground">
            Back to home
          </Link>
        </div>
      </header>
      <main className="container mx-auto px-4 py-12 max-w-3xl">
        <div>
          {blocks.map((block, index) => renderBlock(block.type, block.text, index))}
        </div>
        <p className="mt-4 text-sm text-muted-foreground">
          <Link to="/" className="text-primary hover:underline">Return to home</Link>
          {' · '}
          <Link to="/terms" className="text-primary hover:underline">Terms and Conditions</Link>
        </p>
      </main>
    </div>
  );
}
