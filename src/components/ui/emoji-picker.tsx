// Search index: emoji + keywords (category + common terms) so search matches text
const emojiSearchIndex: { emoji: string; keywords: string }[] = [];
const categoryKeywords: Record<string, string> = {
  'Smileys': 'smileys smile happy face sad emotion emoji',
  'Gestures': 'gestures hand wave thumbs',
  'People': 'people person man woman baby',
  'Animals': 'animals animal dog cat bird',
  'Food': 'food eat drink coffee',
  'Activities': 'activities sport game music',
  'Travel': 'travel car plane train',
  'Objects': 'objects object phone computer',
  'Symbols': 'symbols heart star check',
  'Flags': 'flags flag country',
};
Object.entries(emojiCategories).forEach(([category, emojis]) => {
  const keywords = `${category.toLowerCase()} ${categoryKeywords[category] || ''}`.trim();
  emojis.forEach((emoji) => {
    emojiSearchIndex.push({ emoji, keywords });
  });
});

interface EmojiPickerProps {
  value?: string;
  onChange?: (emoji: string) => void;
  children?: React.ReactNode;
  className?: string;
}

export function EmojiPicker({ value, onChange, children, className }: EmojiPickerProps) {
  const [open, setOpen] = React.useState(false);
  const [search, setSearch] = React.useState('');
  const [activeTab, setActiveTab] = React.useState('Smileys');

  const filteredEmojis = React.useMemo(() => {
    const q = search.trim().toLowerCase();
    if (!q) return null;
    const words = q.split(/\s+/).filter(Boolean);
    return emojiSearchIndex
      .filter(({ keywords }) => {
        const keywordTokens = keywords.toLowerCase().split(/\s+/);
        return words.every((w) =>
          keywordTokens.some((token) => token.startsWith(w) || token.includes(w))
        );
      })
      .map(({ emoji }) => emoji);
  }, [search]);

  const handleSelect = (emoji: string) => {
    onChange?.(emoji);
    setOpen(false);
    setSearch('');
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        {children || (
          <Button variant="outline" className={cn('h-10 w-10 p-0 text-lg', className)}>
            {value || '😀'}
          </Button>
        )}
      </PopoverTrigger>
      <PopoverContent className="w-[320px] p-0" align="start">
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              placeholder="Search emojis..."
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              className="pl-9"
            />
          </div>
        </div>
        
        {search ? (
          <ScrollArea className="h-[250px] p-3">
            <div className="grid grid-cols-8 gap-1">
              {filteredEmojis?.length ? (
                filteredEmojis.map((emoji, i) => (
                  <button
                    key={`${emoji}-${i}`}
                    onClick={() => handleSelect(emoji)}
                    className="h-8 w-8 flex items-center justify-center rounded hover:bg-muted transition-colors text-lg"
                  >
                    {emoji}
                  </button>
                ))
              ) : (
                <p className="col-span-8 text-center text-muted-foreground py-4">
                  No emojis found
                </p>
              )}
            </div>
          </ScrollArea>
        ) : (
          <Tabs value={activeTab} onValueChange={setActiveTab} className="w-full">
            <ScrollArea className="w-full">
              <TabsList className="w-full justify-start h-auto p-1 bg-transparent border-b rounded-none">
                {Object.keys(emojiCategories).map((category) => (
                  <TabsTrigger
                    key={category}
                    value={category}
                    className="text-xs px-2 py-1.5 data-[state=active]:bg-muted"
                  >
                    {category === 'Smileys' && '😀'}
                    {category === 'Gestures' && '👋'}
                    {category === 'People' && '👤'}
                    {category === 'Animals' && '🐶'}
                    {category === 'Food' && '🍕'}
                    {category === 'Activities' && '⚽'}
                    {category === 'Travel' && '🚗'}
                    {category === 'Objects' && '💡'}
                    {category === 'Symbols' && '❤️'}
                    {category === 'Flags' && '🏳️'}
                  </TabsTrigger>
                ))}
              </TabsList>
            </ScrollArea>
            {Object.entries(emojiCategories).map(([category, emojis]) => (
              <TabsContent key={category} value={category} className="m-0">
                <ScrollArea className="h-[200px] p-3">
                  <div className="grid grid-cols-8 gap-1">
                    {emojis.map((emoji, i) => (
                      <button
                        key={`${emoji}-${i}`}
                        onClick={() => handleSelect(emoji)}
                        className="h-8 w-8 flex items-center justify-center rounded hover:bg-muted transition-colors text-lg"
                      >
                        {emoji}
                      </button>
                    ))}
                  </div>
                </ScrollArea>
              </TabsContent>
            ))}
          </Tabs>
        )}
      </PopoverContent>
    </Popover>
  );
}
